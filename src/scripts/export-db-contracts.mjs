import fs from "node:fs";
import path from "node:path";
import { Client } from "pg";

const TARGET_RPCS = [
  "get_personalized_feed",
  "calculate_ai_med_score",
  "get_journal_tier_and_weight",
  "get_or_flag_keyword",
  "build_pubmed_query_for_keyword",
  "save_llm_synonyms",
];

function loadDotEnv(filePath) {
  if (!fs.existsSync(filePath)) return;
  const content = fs.readFileSync(filePath, "utf8");
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (!process.env[key]) process.env[key] = value;
  }
}

function quoteIdent(value) {
  return `"${String(value).replaceAll('"', '""')}"`;
}

function qname(schema, name) {
  return `${quoteIdent(schema)}.${quoteIdent(name)}`;
}

function roleName(value) {
  return value === "public" ? "public" : quoteIdent(value);
}

function literal(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function asArray(value) {
  return Array.isArray(value) ? value : [];
}

function groupBy(rows, key) {
  const groups = new Map();
  for (const row of rows) {
    const groupKey = row[key];
    if (!groups.has(groupKey)) groups.set(groupKey, []);
    groups.get(groupKey).push(row);
  }
  return groups;
}

function renderColumn(column) {
  const parts = [quoteIdent(column.column_name), column.data_type];
  if (column.attidentity === "a") {
    parts.push("generated always as identity");
  } else if (column.attidentity === "d") {
    parts.push("generated by default as identity");
  } else if (column.attgenerated) {
    parts.push(`generated always as (${column.default_expr}) stored`);
  } else if (column.default_expr) {
    parts.push(`default ${column.default_expr}`);
  }
  if (column.attnotnull) parts.push("not null");
  return `  ${parts.join(" ")}`;
}

function renderPolicy(policy) {
  const roles = asArray(policy.roles).map(roleName).join(", ");
  const lines = [
    `drop policy if exists ${quoteIdent(policy.policyname)} on ${qname(policy.schemaname, policy.tablename)};`,
    `create policy ${quoteIdent(policy.policyname)}`,
    `on ${qname(policy.schemaname, policy.tablename)}`,
    `as ${policy.permissive.toLowerCase()}`,
    `for ${policy.cmd.toLowerCase()}`,
  ];
  if (roles) lines.push(`to ${roles}`);
  if (policy.qual) lines.push(`using (${policy.qual})`);
  if (policy.with_check) lines.push(`with check (${policy.with_check})`);
  return `${lines.join("\n")};`;
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function stamp() {
  return new Date().toISOString().replace(/[-:]/g, "").replace(/\.\d{3}Z$/, "Z");
}

function getExportDbUrl() {
  const candidates = [
    "SUPABASE_EXPORT_DB_URL",
    "DIRECT_URL",
    "DATABASE_URL",
    "SUPABASE_DB_URL",
  ];
  for (const name of candidates) {
    const value = process.env[name]?.trim();
    if (value) return { name, value };
  }
  throw new Error(`Missing database URL. Set one of: ${candidates.join(", ")}`);
}

function pgConnectionConfig(dbUrl) {
  const url = new URL(dbUrl);
  const sslMode = url.searchParams.get("sslmode");
  url.searchParams.delete("sslmode");
  url.searchParams.delete("sslcert");
  url.searchParams.delete("sslkey");
  url.searchParams.delete("sslrootcert");
  return {
    connectionString: url.toString(),
    ssl: sslMode === "disable" ? false : { rejectUnauthorized: false },
    application_name: "zlab_contract_export_readonly",
  };
}

async function main() {
  const root = process.cwd();
  loadDotEnv(path.join(root, ".env.local"));

  const { name: dbUrlName, value: dbUrl } = getExportDbUrl();

  const client = new Client(pgConnectionConfig(dbUrl));

  const exportedAt = new Date().toISOString();
  await client.connect();
  try {
    await client.query("set statement_timeout = '30s'");
    await client.query("set default_transaction_read_only = on");
    await client.query("begin read only");

    const extensions = await client.query(`
        select e.extname, n.nspname as schema_name
        from pg_extension e
        join pg_namespace n on n.oid = e.extnamespace
        where e.extname <> 'plpgsql'
        order by e.extname
      `);
    const enums = await client.query(`
        select n.nspname as schema_name, t.typname as type_name,
          array_agg(e.enumlabel order by e.enumsortorder) as labels
        from pg_type t
        join pg_namespace n on n.oid = t.typnamespace
        join pg_enum e on e.enumtypid = t.oid
        where n.nspname = 'public'
        group by n.nspname, t.typname
        order by t.typname
      `);
    const tables = await client.query(`
        select n.nspname as schema_name, c.relname as table_name, c.relkind
        from pg_class c
        join pg_namespace n on n.oid = c.relnamespace
        where n.nspname = 'public'
          and c.relkind in ('r', 'p')
        order by c.relname
      `);
    const columns = await client.query(`
        select n.nspname as schema_name, c.relname as table_name, a.attname as column_name,
          a.attnum, format_type(a.atttypid, a.atttypmod) as data_type,
          a.attnotnull, a.attidentity, a.attgenerated,
          pg_get_expr(ad.adbin, ad.adrelid) as default_expr
        from pg_attribute a
        join pg_class c on c.oid = a.attrelid
        join pg_namespace n on n.oid = c.relnamespace
        left join pg_attrdef ad on ad.adrelid = a.attrelid and ad.adnum = a.attnum
        where n.nspname = 'public'
          and c.relkind in ('r', 'p')
          and a.attnum > 0
          and not a.attisdropped
        order by c.relname, a.attnum
      `);
    const constraints = await client.query(`
        select con.oid, con.conname, con.contype,
          n.nspname as schema_name, c.relname as table_name,
          pg_get_constraintdef(con.oid, true) as definition
        from pg_constraint con
        join pg_class c on c.oid = con.conrelid
        join pg_namespace n on n.oid = con.connamespace
        where n.nspname = 'public'
        order by c.relname, con.contype, con.conname
      `);
    const indexes = await client.query(`
        select cn.nspname as schema_name, tc.relname as table_name, ic.relname as index_name,
          pg_get_indexdef(i.indexrelid) as definition,
          con.oid is not null as constraint_backed
        from pg_index i
        join pg_class ic on ic.oid = i.indexrelid
        join pg_class tc on tc.oid = i.indrelid
        join pg_namespace cn on cn.oid = tc.relnamespace
        left join pg_constraint con on con.conindid = i.indexrelid
        where cn.nspname = 'public'
        order by tc.relname, ic.relname
      `);
    const functions = await client.query(`
        select p.oid, p.proname, pg_get_function_identity_arguments(p.oid) as identity_args,
          pg_get_functiondef(p.oid) as definition
        from pg_proc p
        join pg_namespace n on n.oid = p.pronamespace
        where n.nspname = 'public'
        order by p.proname, pg_get_function_identity_arguments(p.oid)
      `);
    const triggers = await client.query(`
        select n.nspname as schema_name, c.relname as table_name, t.tgname as trigger_name,
          pg_get_triggerdef(t.oid, true) as definition
        from pg_trigger t
        join pg_class c on c.oid = t.tgrelid
        join pg_namespace n on n.oid = c.relnamespace
        where n.nspname = 'public'
          and not t.tgisinternal
        order by c.relname, t.tgname
      `);
    const views = await client.query(`
        select n.nspname as schema_name, c.relname as view_name, c.relkind,
          pg_get_viewdef(c.oid, true) as definition
        from pg_class c
        join pg_namespace n on n.oid = c.relnamespace
        where n.nspname = 'public'
          and c.relkind in ('v', 'm')
        order by c.relname
      `);
    const policies = await client.query(`
        select schemaname, tablename, policyname, permissive, roles, cmd, qual, with_check
        from pg_policies
        where schemaname = 'public'
        order by tablename, policyname
      `);
    const rlsTables = await client.query(`
        select n.nspname as schema_name, c.relname as table_name,
          c.relrowsecurity, c.relforcerowsecurity
        from pg_class c
        join pg_namespace n on n.oid = c.relnamespace
        where n.nspname = 'public'
          and c.relkind in ('r', 'p')
        order by c.relname
      `);

    await client.query("rollback");

    const columnsByTable = groupBy(columns.rows, "table_name");
    const rlsByTable = new Map(rlsTables.rows.map((row) => [row.table_name, row]));
    const exportedFunctionNames = new Set(functions.rows.map((row) => row.proname));
    const presentRpcs = TARGET_RPCS.filter((name) => exportedFunctionNames.has(name));
    const missingRpcs = TARGET_RPCS.filter((name) => !exportedFunctionNames.has(name));

    const lines = [
      "-- Remote Supabase public schema contract export.",
      "-- Generated by src/scripts/export-db-contracts.mjs.",
      "-- Read-only source operation: metadata queries inside BEGIN READ ONLY.",
      "-- Do not run this file directly against production without DBA review.",
      `-- Exported at: ${exportedAt}`,
      "",
      "create schema if not exists public;",
      "",
    ];

    if (extensions.rows.length) {
      lines.push("-- Extensions");
      for (const ext of extensions.rows) {
        lines.push(`create extension if not exists ${quoteIdent(ext.extname)} with schema ${quoteIdent(ext.schema_name)};`);
      }
      lines.push("");
    }

    if (enums.rows.length) {
      lines.push("-- Enum types");
      for (const en of enums.rows) {
        lines.push(`create type ${qname(en.schema_name, en.type_name)} as enum (${asArray(en.labels).map(literal).join(", ")});`);
      }
      lines.push("");
    }

    if (tables.rows.length) {
      lines.push("-- Tables");
      for (const table of tables.rows) {
        const cols = columnsByTable.get(table.table_name) ?? [];
        lines.push(`create table ${qname(table.schema_name, table.table_name)} (`);
        lines.push(cols.map(renderColumn).join(",\n"));
        lines.push(");");
        lines.push("");
      }
    }

    if (constraints.rows.length) {
      lines.push("-- Constraints");
      for (const con of constraints.rows) {
        lines.push(
          `alter table only ${qname(con.schema_name, con.table_name)} add constraint ${quoteIdent(con.conname)} ${con.definition};`,
        );
      }
      lines.push("");
    }

    if (functions.rows.length) {
      lines.push("-- Functions and RPCs");
      for (const fn of functions.rows) {
        lines.push(fn.definition.trimEnd());
        lines.push("");
      }
    }

    if (views.rows.length) {
      lines.push("-- Views and materialized views");
      for (const view of views.rows) {
        const kind = view.relkind === "m" ? "materialized view" : "view";
        lines.push(`create ${kind} ${qname(view.schema_name, view.view_name)} as`);
        lines.push(`${view.definition.trimEnd()};`);
        lines.push("");
      }
    }

    const nonConstraintIndexes = indexes.rows.filter((idx) => !idx.constraint_backed);
    if (nonConstraintIndexes.length) {
      lines.push("-- Non-constraint indexes");
      for (const idx of nonConstraintIndexes) {
        lines.push(`${idx.definition};`);
      }
      lines.push("");
    }

    const enabledRls = rlsTables.rows.filter((row) => row.relrowsecurity || row.relforcerowsecurity);
    if (enabledRls.length) {
      lines.push("-- Row level security");
      for (const row of enabledRls) {
        if (row.relrowsecurity) {
          lines.push(`alter table ${qname(row.schema_name, row.table_name)} enable row level security;`);
        }
        if (row.relforcerowsecurity) {
          lines.push(`alter table ${qname(row.schema_name, row.table_name)} force row level security;`);
        }
      }
      lines.push("");
    }

    if (policies.rows.length) {
      lines.push("-- RLS policies");
      for (const policy of policies.rows) {
        lines.push(renderPolicy(policy));
        lines.push("");
      }
    }

    if (triggers.rows.length) {
      lines.push("-- Triggers");
      for (const trigger of triggers.rows) {
        lines.push(`drop trigger if exists ${quoteIdent(trigger.trigger_name)} on ${qname(trigger.schema_name, trigger.table_name)};`);
        lines.push(`${trigger.definition};`);
        lines.push("");
      }
    }

    const exportStamp = stamp();
    const exportDir = path.join(root, "sql", "exports");
    const docsDir = path.join(root, "docs", "sql-change-proposals");
    ensureDir(exportDir);
    ensureDir(docsDir);

    const sqlPath = path.join(exportDir, `${exportStamp}_remote_public_schema.sql`);
    const docPath = path.join(docsDir, `${exportStamp}_supabase_schema_rpc_handoff.md`);
    fs.writeFileSync(sqlPath, `${lines.join("\n").trimEnd()}\n`, "utf8");

    const doc = [
      "# Supabase Schema/RPC Handoff",
      "",
      `Generated at: ${exportedAt}`,
      "",
      "## Safety Boundary",
      "",
      "- Codex performed read-only catalog queries inside `BEGIN READ ONLY`.",
      "- Codex did not run DDL against the remote database.",
      "- Treat the SQL export as a review artifact. Claude/DB owner should decide how to split it into real migrations.",
      "",
      "## Files",
      "",
      `- SQL export: \`${path.relative(root, sqlPath).replaceAll("\\", "/")}\``,
      "",
      "## Export Summary",
      "",
      `- Public tables: ${tables.rows.length}`,
      `- Public functions/RPCs: ${functions.rows.length}`,
      `- RLS policies: ${policies.rows.length}`,
      `- Non-constraint indexes: ${nonConstraintIndexes.length}`,
      `- Triggers: ${triggers.rows.length}`,
      "",
      "## Application RPC Coverage",
      "",
      ...TARGET_RPCS.map((name) => `- ${exportedFunctionNames.has(name) ? "[x]" : "[ ]"} \`${name}\``),
      "",
      missingRpcs.length
        ? `Missing RPCs from export: ${missingRpcs.map((name) => `\`${name}\``).join(", ")}`
        : "All application RPCs listed in the review findings were found in the remote public schema export.",
      "",
      "## Request For Claude/DB Owner",
      "",
      "1. Review the SQL export and confirm it matches the intended production database contract.",
      "2. Convert the accepted parts into ordered, idempotent Supabase migrations.",
      "3. Keep production DDL ownership outside Codex unless explicitly approved.",
      "4. Pay special attention to the RPCs used by app code and the `feed_recommendations` upsert constraint.",
      "",
    ].join("\n");
    fs.writeFileSync(docPath, doc, "utf8");

    console.log(`Database URL env: ${dbUrlName}`);
    console.log(`Exported SQL: ${path.relative(root, sqlPath)}`);
    console.log(`Handoff doc: ${path.relative(root, docPath)}`);
    console.log(`RPCs found: ${presentRpcs.length}/${TARGET_RPCS.length}`);
    if (missingRpcs.length) {
      console.log(`RPCs missing: ${missingRpcs.join(", ")}`);
    }
  } finally {
    await client.end().catch(() => {});
  }
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
